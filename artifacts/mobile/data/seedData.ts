export interface User {
  id: string;
  username: string;
  displayName: string;
  avatarColor: string;
  avatarInitials: string;
  bio: string;
  genre: string;
  location: string;
  followersCount: number;
  followingCount: number;
  totalLikes: number;
  totalGoldenMics: number;
  riseScore: number;
  liveEligible: boolean;
  postsCount: number;
  isCreator: boolean;
  badges: string[];
  imageIndex: number;
}

export interface LyricSectionData {
  sectionId: string;
  sectionLabel: string;
  trackId: string;
  startMs: number;
  endMs: number;
  startLineIndex?: number;
  endLineIndex?: number;
  lineCount: number;
  timingMode?: string;
  timingOffsetMs: number;
  language: string;
}

export interface MusicMinute {
  id: string;
  userId: string;
  title: string;
  caption: string;
  performanceType: "original" | "cover" | "freestyle";
  genre: string;
  language: string;
  location: string;
  tags: string[];
  musixmatchTrackId?: string;
  trackTitle?: string;
  trackArtist?: string;
  imageIndex: number;
  videoUri?: string;
  views: number;
  likesCount: number;
  commentsCount: number;
  sharesCount: number;
  savesCount: number;
  goldenMicsCount: number;
  createdAt: string;
  isRisingVoice: boolean;
  isFeatured: boolean;
  lyricSection?: LyricSectionData;
  moodTags?: string[];
  /** Populated for API-backed posts; seed posts use getUserById() instead */
  creatorDisplayName?: string;
  creatorUsername?: string;
  creatorAvatarUrl?: string | null;
}

export interface SeedComment {
  id: string;
  userId: string;
  username: string;
  displayName: string;
  avatarColor: string;
  content: string;
  musicMinuteId: string;
  createdAt: string;
}

